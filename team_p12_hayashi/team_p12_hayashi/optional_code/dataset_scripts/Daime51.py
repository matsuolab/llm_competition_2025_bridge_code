math_df = pd.read_csv('./AIME2024_Math_30_a.csv', encoding='cp932')
# # -------------------------------------------------------
import requests
# # -------------------------------------------------------
# ▼ 2. 概要問い合わせ（テキストベースのみ）
# # -----------------------------------------------------
mc_list = []
for k in range(len(math_df)):
    # 参考資料として読み込む外部テキストファイル
    # テキストファイルの内容を読み込む
    problem_text = math_df['Problem'][k]
    solution_text = math_df['Solution'][k]
    answer_text = math_df['Answer'][k]
    # 複数行の文字列プロンプト
    summary_prompt = f"""The following 【problem】, 【solution】 and 【answer】 are from an AIME-level 
    mathematics question, its solution and its answer.
    Your task is to extract and summarize the key steps of the solution in a step-by-step manner.
    Then, create a hint-enhanced version of the original problem by **adding** the summarized steps
    **after** the problem statement.
    Important instructions:
    - DO NOT modify the original problem text.
    - Begin the hint section with the sentence: "However, please refer to the following steps for guidance."
    - Each step must begin with "Step (number):"
    - DO NOT include the final answer 【answer】 anywhere in the step-by-step hints.
    - **Make sure that each step is complete and self-contained. Do not cut off steps halfway through; each should represent a logically complete idea or operation.**
    - Your output must be written in English only.
    【problem】
    {problem_text}
    【solution】
    {solution_text}
    【answer】
    {answer_text}
    """
    system_prompt_text = f"""You are a brilliant mathematics expert and educator.
    Your job is to help students understand and solve challenging problems by providing structured,
    step-by-step guidance without revealing the final answer.
    Ensure that each step is complete and not cut off mid-thought.
    """
    # セッションとテキストのフォームデータ
    data = {
        "sessionId": "xxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxx",
        "userPromptText": summary_prompt,
        "systemPromptText": system_prompt_text
    }
    response = requests.post("https://aaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaa",
                             data=data, verify=False)
    response_json = json.loads(response.text)
    exp01 = response_json.get("Text", "")
    # リストに追加
    mc_list.append(exp01)
# DataFrame に列として追加
math_df['QwFullH'] = mc_list
# 出力 CSV ファイル名（任意で調整）
output_file = './AIME2024_Math_30_a_QwFullH.csv'
math_df.to_csv(output_file, index=False, encoding='utf-8-sig')
print(f"保存完了: {output_file}")
